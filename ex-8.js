//GET HIGHEST VALUE & ADJ NUMBERS FROM AN ARRAY
function getMax(n) {
  var i;
  var newArr = [];
  for (i = 0; i <= 36; i++) {
    var selectArr = n.slice(i, i + 13);
    newArr.push(selectArr);
    var multiplyArr = newArr.map((item) => item.reduce((a, b) => a * b, 1));
  }
  var adjNum = newArr.find(
    (item) => item.reduce((a, b) => a * b, 1) == Math.max(...multiplyArr)
  );
  maxValues.push(Math.max(...multiplyArr));
  adjMaxNum.push(adjNum);
  //console.log(`O valor máximo é ${maxValue}`);
  //console.log(`O produto é composto por ${maxAdjNum}`);
}

